    private void loadbuilingId() {
        try {
            DBNEW.closeConn();
            DBNEW.createCon();
            ResultSet rs = DBNEW.search("select * from buildings");

            Vector v = new Vector();
            v.add("None");
            while (rs.next()) {

                v.add(rs.getString("buildingid"));
                jComboBox1.setModel(new DefaultComboBoxModel(v));

            }
            rs.close();
            DBNEW.closeConn();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

