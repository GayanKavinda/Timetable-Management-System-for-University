    private void loadDetails() {

        try {
            DBNEW.closeConn();
            DBNEW.createCon();
            ResultSet rs = DBNEW.search("select * from reserveroomforexam");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("buidingid"));
                v.add(rs.getString("roomid"));
                v.add(rs.getString("roomtype"));
                v.add(rs.getString("date"));
                v.add(rs.getString("timefrom"));
                v.add(rs.getString("capacity"));
                v.add(rs.getString("id"));

                dtm.addRow(v);
            }
            rs.close();
            DBNEW.closeConn();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
