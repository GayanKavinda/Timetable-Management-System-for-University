    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        try {

            String roomid = jComboBox2.getSelectedItem().toString();
            if (roomid.equals("None")) {
                jTextField1.setText("");
            } else {
                DBNEW.closeConn();
                DBNEW.createCon();
                ResultSet rs = DBNEW.search("select * from rooms where roomid='" + roomid + "'");

                if (rs.next()) {

                    jTextField1.setText(rs.getString("roomtype"));

                }
                rs.close();
                DBNEW.closeConn();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jComboBox2ItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jTextField4.getText().equals("") || jTextField5.getText().equals("") || jComboBox1.getSelectedItem().toString().equals("None") || jComboBox2.getSelectedItem().toString().equals("None")) {
                JOptionPane.showMessageDialog(this, "Fill all feilds to continue..!", "Done", JOptionPane.ERROR_MESSAGE);
            } else if (jDateChooser1.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Select Valid date..!", "Done", JOptionPane.ERROR_MESSAGE);
            } else {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
                String date = sdf.format(jDateChooser1.getDate());
                DBNEW.closeConn();
                DBNEW.createCon();
                DBNEW.iud("insert into reserveroomforexam(buidingid,roomid,roomtype,date,timefrom,capacity) values('" + jComboBox1.getSelectedItem().toString() + "','" + jComboBox2.getSelectedItem().toString() + "','" + jTextField1.getText() + "','" + date + "','" + jTextField4.getText() + "','" + jTextField5.getText() + "')");
                DBNEW.closeConn();
                JOptionPane.showMessageDialog(this, "Data Saved..!", "Done", JOptionPane.OK_OPTION);
                jTextField1.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jDateChooser1.setDate(null);
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                loadDetails();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            int i = jTable1.getSelectedRow();
            if (i < 0) {
                JOptionPane.showMessageDialog(this, "Select Valid ROW", "Done", JOptionPane.ERROR_MESSAGE);

            } else {

                String buildingid = jTable1.getValueAt(i, 0).toString();
                String roomid = jTable1.getValueAt(i, 1).toString();
                String roomtype = jTable1.getValueAt(i, 2).toString();
                String date = jTable1.getValueAt(i, 3).toString();
                String startfrom = jTable1.getValueAt(i, 4).toString();
                String endto = jTable1.getValueAt(i, 5).toString();
                String id = jTable1.getValueAt(i, 6).toString();
                new EditReservedStudy(buildingid, roomid, roomtype, date, startfrom, endto, id).setVisible(true);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

