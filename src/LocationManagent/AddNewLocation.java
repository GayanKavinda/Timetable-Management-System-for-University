    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            if (jTextField1.getText().equals("") || jTextField2.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Fill all feilds to continue..!", "Done", JOptionPane.ERROR_MESSAGE);

            } else {
                DBNEW.closeConn();
                DBNEW.createCon();
                ResultSet rs = DBNEW.search("select * from buildings where buildingid='" + jTextField2.getText() + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Building ID already exists..!", "Done", JOptionPane.ERROR_MESSAGE);
                    rs.close();
                    DBNEW.closeConn();
                } else {
                    rs.close();
                    DBNEW.closeConn();
                    DBNEW.createCon();
                    DBNEW.iud("insert into buildings(buildingid,buildingname) values('" + jTextField2.getText() + "','" + jTextField1.getText() + "')");
                    DBNEW.closeConn();
                    JOptionPane.showMessageDialog(this, "New Building Saved..!", "Done", JOptionPane.OK_OPTION);
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField2.grabFocus();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

