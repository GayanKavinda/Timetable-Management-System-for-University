    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            if (jTextField3.getText().equals("") || jComboBox1.getSelectedItem().toString().equals("None")) {
                JOptionPane.showMessageDialog(this, "Fill all feilds to continue..!", "Done", JOptionPane.ERROR_MESSAGE);

            } else {
                DBNEW.closeConn();
                DBNEW.createCon();
                ResultSet rs = DBNEW.search("select * from rooms where roomid='" + jTextField3.getText() + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Room ID already exists..!", "Done", JOptionPane.ERROR_MESSAGE);
                    rs.close();
                    DBNEW.closeConn();
                } else {
                    rs.close();
                    DBNEW.closeConn();
                    DBNEW.createCon();
                    DBNEW.iud("insert into rooms(roomid,roomtype) values('" + jTextField3.getText() + "','" + jComboBox1.getSelectedItem().toString() + "')");
                    DBNEW.closeConn();
                    JOptionPane.showMessageDialog(this, "New Room Saved..!", "Done", JOptionPane.OK_OPTION);
                    jTextField3.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jTextField3.grabFocus();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

