    private ArrayList<DegreeP> getDPList() {
         
        ArrayList<DegreeP> list = new ArrayList<DegreeP>();
        try{
        connection = DBConnection.getConnection();
        String querry = "select * from degree_program";
        Statement st;
        ResultSet rs;
        
        st = connection.createStatement();
        rs= st.executeQuery(querry);
       DegreeP dP;
        while(rs.next())
        {
            dP = new DegreeP(rs.getInt("id"), rs.getString("dp"));
            list.add(dP);
        }
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        return list;
    }
