    private void btn_groupIDMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_groupIDMousePressed
        
        String ys = select_ys.getSelectedItem().toString();
        String dp = select_dp.getSelectedItem().toString();
        String gno = select_gno.getSelectedItem().toString();
        
        
        if(select_ys.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(idGenerate.this, "Lecturer id cannot empty! ", "Data missing", JOptionPane.ERROR_MESSAGE);
        }
        else if(select_dp.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(idGenerate.this, "Lecture id Should be 6 characters", "Data missing", JOptionPane.ERROR_MESSAGE);
        }
        else{
            txt_groupID.setText(ys + "." + dp + "." + gno);
            
        }
    }//GEN-LAST:event_btn_groupIDMousePressed

    private void btn_saveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMousePressed
        
        boolean error = false;
        try{
            DefaultTableModel model = (DefaultTableModel)tbl_groupID.getModel();
            connection = DBConnection.getConnection();
            
            
            if(txt_groupID.getText().contentEquals("")){
                 JOptionPane.showMessageDialog(idGenerate.this, "Group ID cannot be empty!", "Data missing", JOptionPane.ERROR_MESSAGE);
                 txt_error.setText("Generate ID");
                  error = true;
            }
            else{
                txt_error.setText("");
                
                PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.INSERT_GROUP_ID));
                statement.setString(1, txt_groupID.getText().toUpperCase());
                statement.executeUpdate();
                model.setRowCount(0);
                
                showGroupIDList();
                txt_groupID.setText("");
                JOptionPane.showMessageDialog(null, "Successfull Added Group ID");
                
            }
            
            
        }catch(SQLException | ClassNotFoundException | IOException | ParserConfigurationException | SAXException ex){
            Logger.getLogger(GenerateID.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }//GEN-LAST:event_btn_saveMousePressed

