    private ArrayList<GenerateID> getGroupIDList() {
        ArrayList<GenerateID> list = new ArrayList<GenerateID>();
        try{
        connection = DBConnection.getConnection();
        String querry = "select * from group_id";
        Statement st;
        ResultSet rs;
        
        st = connection.createStatement();
        rs= st.executeQuery(querry);
        GenerateID GenrateID;
        while(rs.next())
        {
            GenrateID = new GenerateID(rs.getInt("id"), rs.getString("gId"));
            list.add(GenrateID);
        }
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        return list;
    }
       
