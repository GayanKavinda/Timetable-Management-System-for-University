    private ArrayList<GroupNo> getGnoList() {
        
         ArrayList<GroupNo> list = new ArrayList<GroupNo>();
        try{
        connection = DBConnection.getConnection();
        String querry = "select * from groupNo";
        Statement st;
        ResultSet rs;
        
        st = connection.createStatement();
        rs= st.executeQuery(querry);
        GroupNo gno;
        while(rs.next())
        {
            gno = new GroupNo(rs.getInt("id"), rs.getString("gNo"));
            list.add(gno);
        }
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        return list;
       
    }
