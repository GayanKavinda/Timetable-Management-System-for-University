    private void btn_addMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_addMousePressed
       
        boolean error = false;
        
        try{
            DefaultTableModel model = (DefaultTableModel)tbl_gno.getModel();
            connection = DBConnection.getConnection();
            
            if(txt_gno.getText().contentEquals("")){
                 JOptionPane.showMessageDialog(addGroupNum.this, "Group Number cannot be empty!", "Data missing", JOptionPane.ERROR_MESSAGE);
                 txt_error.setText("Enter Group Number");
                  error = true;
            }
            else{
                txt_error.setText("");
                
                PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.INSERT_GROUP_NUMBER));
                statement.setString(1, txt_gno.getText().toUpperCase());
                statement.executeUpdate();
                model.setRowCount(0);
                
                showGnoList();
                txt_gno.setText("");
                JOptionPane.showMessageDialog(null,"Successful Added Group Number !");
                     
            }
         }catch (SQLException | ClassNotFoundException | IOException |
                ParserConfigurationException | SAXException ex) {
                Logger.getLogger(AcademicYS.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btn_addMousePressed

