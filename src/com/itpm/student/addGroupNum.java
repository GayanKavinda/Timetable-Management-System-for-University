    private void showGnoList() {
        
        ArrayList<GroupNo> list = getGnoList();
        DefaultTableModel model = (DefaultTableModel)tbl_gno.getModel();
        Object[] row = new Object[4];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getgNo();
            row[2] = "Edit";
            row[3] = "Delete";
            
            model.addRow(row);
        }
         
        
    }

    private void deleteData(int row) {
        
         boolean error = false;
         DefaultTableModel mod = (DefaultTableModel) tbl_gno.getModel();
        
         try {
             int id = (int) mod.getValueAt(row, 0);
            
             
            Connection connection =  DBConnection.getConnection();
            PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.DELETE_GROUP_NUMBER));
            
            statement.setInt(1, id);
            statement.execute();
            
        } catch (SQLException | ClassNotFoundException | IOException | 
                ParserConfigurationException | SAXException ex) {
            Logger.getLogger(addAcademicYS.class.getName()).log(Level.SEVERE, null, ex);
            error = true;
            
             JOptionPane.showMessageDialog(addGroupNum.this, "Delete Error", "Error", JOptionPane.ERROR_MESSAGE);
        }
         
         if(!error){
             JOptionPane.showMessageDialog(addGroupNum.this, "Delete Succes", "Succes", JOptionPane.DEFAULT_OPTION);
             mod.removeRow(row); 
         }
       
    }

