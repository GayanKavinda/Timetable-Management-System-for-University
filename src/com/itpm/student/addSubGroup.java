    private void btn_addMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_addMousePressed
        
         boolean error = false;
         
         try{
            DefaultTableModel model = (DefaultTableModel)tbl_subGno.getModel();
            connection = DBConnection.getConnection();
            
            if(txt_subGno.getText().contentEquals("")){
                 JOptionPane.showMessageDialog(addSubGroup.this, "Sub Group Number cannot be empty!", "Data missing", JOptionPane.ERROR_MESSAGE);
                 txt_error.setText("Enter Group Number");
                  error = true;
            }
            else{
                txt_error.setText("");
                
                PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.INSERT_SUB_GROUP_NUMBER));
                statement.setString(1, txt_subGno.getText().toUpperCase());
                statement.executeUpdate();
                model.setRowCount(0);
                
                showSubGnoList();
                txt_subGno.setText("");
                JOptionPane.showMessageDialog(null,"Successful Added Sub Group Number !");
                     
            }
         }catch (SQLException | ClassNotFoundException | IOException |
                ParserConfigurationException | SAXException ex) {
                Logger.getLogger(SubGno.class.getName()).log(Level.SEVERE, null, ex);
            }
        
         
    }//GEN-LAST:event_btn_addMousePressed

