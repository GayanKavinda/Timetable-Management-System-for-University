    private ArrayList<SubGno> getSubGnoList() {
        
        ArrayList<SubGno> list = new ArrayList<SubGno>();
        try{
        connection = DBConnection.getConnection();
        String querry = "select * from subGroupNo";
        Statement st;
        ResultSet rs;
        
        st = connection.createStatement();
        rs= st.executeQuery(querry);
        SubGno subGno;
        while(rs.next())
        {
            subGno = new SubGno(rs.getInt("id"), rs.getString("SGno"));
            list.add(subGno);
        }
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        return list;
    }
