    private void btn_addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_addMouseClicked
        
        
        boolean error = false;
        
        if(txt_tagID.getText().contentEquals("")){
            JOptionPane.showMessageDialog(this, "Tag ID  cannot be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
        if(txt_sub.getText().contentEquals("")){
            JOptionPane.showMessageDialog(this, "Subject Name cannot be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
         if(txt_code.getText().contentEquals("")){
            JOptionPane.showMessageDialog(this, "Subject Code cannot be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
         try{
             connection = DBConnection.getConnection();
             PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.INSERT_TAG));
             
             tag.setTagID(txt_tagID.getText());
             tag.setSubject(txt_sub.getText());
             tag.setCode(txt_code.getText());
             tag.setrTag(txt_rtag.getText());
             
             statement.setString(1, tag.getTagID());
             statement.setString(2, tag.getSubject());
             statement.setString(3, tag.getCode());
             statement.setString(4, tag.getrTag());
            
         }catch (SQLException | ClassNotFoundException | IOException | 
                    ParserConfigurationException | SAXException ex) {
                Logger.getLogger(addTag.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error on data save", "Data error", JOptionPane.ERROR_MESSAGE);
                error = true;
            }
         if(!error){
                    JOptionPane.showMessageDialog(this, "Tag Added", "Add Subject", JOptionPane.DEFAULT_OPTION);
                    viewTag vs = new viewTag();
                    vs.setVisible(true);
                    
                    
            }
    }//GEN-LAST:event_btn_addMouseClicked

