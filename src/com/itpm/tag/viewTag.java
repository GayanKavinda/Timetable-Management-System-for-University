    private void execute() {
        
         try{
             Connection connection = DBConnection.getConnection();
             PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.GET_TAG));
             ResultSet resultSet = statement.executeQuery();
             
             while(resultSet.next()){
                 DefaultTableModel mod = (DefaultTableModel) tbl_tag.getModel();
                 Tag tag = new Tag();
                 
                 tag.setId(resultSet.getInt("id"));
                 tag.setTagID(resultSet.getString("tagId"));
                 tag.setSubject(resultSet.getString("subject"));
                 tag.setCode(resultSet.getString("code"));
                 tag.setrTag(resultSet.getString("rTag"));
                 
                 String sub = tag.getSubject().toString();
                 JOptionPane.showMessageDialog(this, "subject" + sub, "Data error", JOptionPane.ERROR_MESSAGE); 
                 
                 tags.add(tag);
                 mod.setRowCount(0);
                createTable();
             }
                 
            }catch(SQLException | ClassNotFoundException | IOException | 
                ParserConfigurationException | SAXException ex) {
            Logger.getLogger(viewTag.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error on data Load", "Data error", JOptionPane.ERROR_MESSAGE); }
                
                  
    }

    private void deleteData(int row) {
        
        boolean error = false;
        DefaultTableModel model = (DefaultTableModel) tbl_tag.getModel();
        
       
         try {
             int id = (int) model.getValueAt(row, 0);
            
             
            Connection connection =  DBConnection.getConnection();
            PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.DELETE_TAG));
            
            statement.setInt(1, id);
            statement.execute();
            
        } catch (SQLException | ClassNotFoundException | IOException | 
                ParserConfigurationException | SAXException ex) {
            Logger.getLogger(viewTag.class.getName()).log(Level.SEVERE, null, ex);
            error = true;
            
             JOptionPane.showMessageDialog(viewTag.this, "Delete Error", "Error", JOptionPane.ERROR_MESSAGE);
        }
         
         if(!error){
             JOptionPane.showMessageDialog(viewTag.this, "Delete Succes", "Succes", JOptionPane.DEFAULT_OPTION);
             model.removeRow(row);
             
             
         }
    }

    private void createTable() {
        
        DefaultTableModel model = (DefaultTableModel) tbl_tag.getModel();
        
        Object [] data = new Object[7];
        
        for(int i = 0;i < tags.size();i++){
            data[0] = tags.get(i).getId();
            data[1] = tags.get(i).getTagID();
            data[2] = tags.get(i).getSubject();
            data[3] = tags.get(i).getCode();
            data[4] = tags.get(i).getrTag();
            data[5] = "Edit";
            data[6] = "Delete";
            
            model.addRow(data);
            
        }
    }
