    private void update_btn_wdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btn_wdActionPerformed
        try {
            //checking hours and minutes empty
            if (!txt_hour_wd.equals("") || txt_minutes_wd.equals("")) {
                int workinghrs = Integer.parseInt(txt_hour_wd.getText());
                int workminutes = Integer.parseInt(txt_minutes_wd.getText());

                //Checking if the zero values inserted to hours and time
                if (workinghrs == 0 && workminutes == 0) {
                    JOptionPane.showMessageDialog(this, "Invalid Minute or hour Count", "Done", JOptionPane.ERROR_MESSAGE);

                } else {

                    int working_day_count = Integer.parseInt(cmb_num_of_days_WD.getSelectedItem().toString());
                    //sorting days
                    String days = "";
                    if (chk_mon.isSelected()) {
                        days += ",Monday";
                    }
                    if (chk_tue.isSelected()) {
                        days += ",Tuesday";
                    }
                    if (chk_wed.isSelected()) {
                        days += ",Wednesday";
                    }
                    if (chk_thu.isSelected()) {
                        days += ",Thursday";
                    }
                    if (chk_fri.isSelected()) {
                        days += ",Friday";
                    }
                    //insert selected dates into array
                    String no_of_days[] = days.split(",");
                    for (int i = 0; i < no_of_days.length; i++) {
                        String no_of_day = no_of_days[i];
                    }

                    int dayscount = no_of_days.length - 1;
                    if (dayscount == working_day_count) {

                        String status = "Weekdays";
                        DBNEW.closeConn();
                        DBNEW.createCon();
                        DBNEW.iud("update work_day_hour set days_no='" + working_day_count + "',days='" + days + "',hour='" + workinghrs + "',mins='" + workminutes + "' where id='" + loaded_id + "'");
                        DBNEW.closeConn();
                        JOptionPane.showMessageDialog(this, "Data Updated", "Done", JOptionPane.OK_OPTION);
                        this.dispose();
                        new ViewWorkingDayHours().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Selected dates must be equal to Selected count", "Done", JOptionPane.ERROR_MESSAGE);

                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_update_btn_wdActionPerformed

