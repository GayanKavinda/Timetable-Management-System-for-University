    private void checkDays(int value) {
        if (value > numOfdays) {
            txt_days_error.setText("Days cannot exceed number of days : " + numOfdays);
        } else {
            txt_days_error.setText("");
        }
    }

    private boolean checkTime() {

        if (Integer.parseInt(txt_hour_wd.getText()) > 12 || Integer.parseInt(txt_hour_wd.getText()) == 0) {
            return true;
        }

        return false;
    }

