    public static void loadWeekdaysData() {
        try {
            DBNEW.closeConn();
            DBNEW.createCon();
            ResultSet rs = DBNEW.search("select * from work_day_hour where status='Weekdays'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector vv = new Vector();
//                vv.add(rs.getString("yearAndSemester"));
                String gette = rs.getString("days_no");
                String gette2 = rs.getString("days");
                String gette2z[] = gette2.split(",");
                ArrayList al = new ArrayList<>();

                for (int i = 0; i < gette2z.length; i++) {
                    String te2z = gette2z[i];
                    al.add(te2z);

                }

                al.remove(0);
                String getdays = "";
                if (al.size() == 1) {
                    getdays = al.get(0).toString();
                } else if (al.size() == 2) {
                    getdays = al.get(0).toString() + "," + al.get(1).toString();
                } else if (al.size() == 3) {
                    getdays = al.get(0).toString() + "," + al.get(1).toString() + "," + al.get(2).toString();
                } else if (al.size() == 4) {
                    getdays = al.get(0).toString() + "," + al.get(1).toString() + "," + al.get(2).toString() + "," + al.get(3).toString();
                } else if (al.size() == 5) {
                    getdays = al.get(0).toString() + "," + al.get(1).toString() + "," + al.get(2).toString() + "," + al.get(3).toString() + "," + al.get(4).toString();
                }
                String gette3 = rs.getString("hour");
                String gette4 = rs.getString("mins");
                String gette5 = rs.getString("id");
                vv.add(gette);
                vv.add(gette3);
                vv.add(gette4);
                vv.add(gette5);
                vv.add(getdays);
                dtm.addRow(vv);
            }
            rs.close();
            DBNEW.closeConn();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

