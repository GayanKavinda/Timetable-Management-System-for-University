    private void btn_submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_submitMouseClicked
        
        if(cmb_lect.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, "Lecaturer name cannot be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
        if(cmb_sub.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this, "Subject  cannot be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
        if(txt_code.getText().contentEquals("")){
            JOptionPane.showMessageDialog(this, "Subject  code be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
        
        try{
            connection = DBConnection.getConnection();
            PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.INSERT_SESSION));
            
            session.setLec(cmb_lect.getSelectedItem().toString());
            session.setLec2(cmb_lect1.getSelectedItem().toString());
            session.setCode(txt_code.getText().toString());
            session.setSubject(cmb_sub.getSelectedItem().toString());
            session.setTag(txt_tag.getText());
            session.setgID(txt_group.getText());
            session.setNoStu(Integer.parseInt(txt_student.getText()));
            session.setDuration(txt_duration.getText());
            
            int stno = session.getNoStu();
            String sno = String.valueOf(stno);
            
            statement.setString(1, session.getLec());
            statement.setString(2, session.getLec2());
            statement.setString(3, session.getCode());
            statement.setString(4, session.getSubject());
            statement.setString(5, session.getTag());
            statement.setString(6, session.getgID());
            statement.setString(7, sno);
            statement.setString(8, session.getDuration());
            
            statement.execute();
        
        }catch (SQLException | ClassNotFoundException | IOException | 
                    ParserConfigurationException | SAXException ex) {
                Logger.getLogger(Session.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error on data save", "Data error", JOptionPane.ERROR_MESSAGE);
                error = true;
            }
             if(!error){
                    JOptionPane.showMessageDialog(this, "Session Added", "Add Session", JOptionPane.DEFAULT_OPTION);
                   ViewSession vl = new ViewSession();
                    vl.setVisible(true);
                    
                    
            }
        
        
    }//GEN-LAST:event_btn_submitMouseClicked

