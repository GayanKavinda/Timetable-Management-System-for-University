    private void lectSelection() {
       try{  
        connection = DBConnection.getConnection();
        String query = "select * from lecturer";
        pst = connection.prepareStatement(query);
        rs = pst.executeQuery();
        
        while(rs.next())
        {
            String lecName = rs.getString("name");
            cmb_lect.addItem(lecName);
            cmb_lect1.addItem(lecName);
        }
        }
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null, e);
        } 
        
    }

    private void subSelection() {
        
        try{  
        connection = DBConnection.getConnection();
        String query = "select * from subject";
        pst = connection.prepareStatement(query);
        rs = pst.executeQuery();
        
        while(rs.next())
        {
            String subName = rs.getString("sub_name");
            cmb_sub.addItem(subName);
        }
        }
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null, e);
        } 
        
    }

    
