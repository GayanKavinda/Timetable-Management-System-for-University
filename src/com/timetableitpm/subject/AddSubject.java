    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked
        
        boolean error = false;
        
        if(txt_subName.getText().contentEquals("")){
            JOptionPane.showMessageDialog(this, "Subject name cannot be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
        if(txt_subCode.getText().contentEquals("")){
            JOptionPane.showMessageDialog(this, "Subject Code cannot be empty", "Data error", JOptionPane.ERROR_MESSAGE);
           
        }
        
        try {
                connection = DBConnection.getConnection();
                PreparedStatement statement = connection.prepareStatement(CreateQuery.getQuery(Constant.INSERT_SUBJECTS));
                
                subject.setSub_yr(cmb_yr.getSelectedItem().toString());
                subject.setSub_sem(cmb_sem.getSelectedItem().toString());
                subject.setSub_name(txt_subName.getText());
                subject.setSub_code(txt_subCode.getText());
                subject.setSub_lectHr(txt_lectHr.getText());
                subject.setSub_tuteHr(txt_tuteHr.getText());
                subject.setSub_labHr(txt_labHr.getText());
                subject.setSub_eveHr(txt_evaHr.getText());
                
                
                
                statement.setString(1, subject.getSub_yr());
                statement.setString(2, subject.getSub_sem());
                statement.setString(3, subject.getSub_name());
                statement.setString(4, subject.getSub_code());
                statement.setString(5, subject.getSub_lectHr());
                statement.setString(6, subject.getSub_tuteHr());
                statement.setString(7, subject.getSub_tuteHr());
                statement.setString(8, subject.getSub_eveHr());
                
                statement.execute();
                } catch (SQLException | ClassNotFoundException | IOException | 
                    ParserConfigurationException | SAXException ex) {
                Logger.getLogger(AddSubject.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error on data save", "Data error", JOptionPane.ERROR_MESSAGE);
                lock1 = true;
            }
                if(!lock1){
                    JOptionPane.showMessageDialog(this, "Subject Added", "Add Subject", JOptionPane.DEFAULT_OPTION);
                    ViewSubject vs = new ViewSubject();
                    vs.setVisible(true);
                    
                    
            }
            
        
    }//GEN-LAST:event_btn_saveMouseClicked

