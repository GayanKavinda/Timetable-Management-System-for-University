    private void btn_rankMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_rankMousePressed
        if(txt_lectID.getText().contentEquals("")){
            JOptionPane.showMessageDialog(EditLecturer.this, "Lecture id cannot be empty!", "Data missing", JOptionPane.ERROR_MESSAGE);
        }
        else if(txt_lectID.getText().length()!= 6){
            JOptionPane.showMessageDialog(EditLecturer.this, "Lecture id Should be 6 characters", "Data missing", JOptionPane.ERROR_MESSAGE);
        }
        else{

            txt_rank.setText(cmb_level.getSelectedItem().toString() + "." + txt_lectID.getText());
        }
    }//GEN-LAST:event_btn_rankMousePressed

    private void btn_updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_updateMouseClicked
         error = false;

        if(txt_rank.getText().contentEquals("")){
            JOptionPane.showMessageDialog(EditLecturer.this, "Rank cannot be empty!", "Data missing", JOptionPane.ERROR_MESSAGE);
            error = true;
        }
        if(txt_name.getText().contentEquals("")){
            JOptionPane.showMessageDialog(EditLecturer.this, "Name cannot be empty!", "Data missing", JOptionPane.ERROR_MESSAGE);
            error = true;
        }
       
        if(!error){
            Lecturer lec = new Lecturer();
            
            try {
                 connection = DBConnection.getConnection();
                 statement = connection.prepareStatement(CreateQuery.getQuery(Constant.UPDATE_LECTURER));
               
            lec.setId(Integer.parseInt(txt_id.getText()));
            lec.setName(txt_name.getText());
            lec.setEmpId(txt_lectID.getText());
            lec.setFaculty(cmb_faculty.getSelectedItem().toString());
            lec.setDepartment(cmb_dept.getSelectedItem().toString());
            lec.setCenter(cmb_center.getSelectedItem().toString());
            lec.setBuilding(cmb_building.getSelectedItem().toString());
            lec.setLevel(cmb_level.getSelectedItem().toString());
            lec.setRank(txt_rank.getText());

                statement.setString(1, lec.getName());
                statement.setString(2, lec.getEmpId());
                statement.setString(3, lec.getFaculty());
                statement.setString(4, lec.getDepartment());
                statement.setString(5, lec.getCenter());
                statement.setString(6, lec.getBuilding());
                statement.setString(7, lec.getLevel());
                statement.setString(8, lec.getRank());
                statement.setInt(9, lec.getId());

                statement.execute();

                
                
               
            } catch (SQLException | ClassNotFoundException | IOException |
                ParserConfigurationException | SAXException ex) {
                Logger.getLogger(AddLecturer.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(EditLecturer.this, "Lecturer Updating Error"+ex.getMessage(), "Lecturer Update Error", JOptionPane.ERROR_MESSAGE);
            }
            if(!error){
                JOptionPane.showMessageDialog(this, "Lecturer Updated", "Lecturer Subject", JOptionPane.DEFAULT_OPTION);
            }
        }
       
    }//GEN-LAST:event_btn_updateMouseClicked

